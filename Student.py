'''
This is how to create a class and student class is a sample for us
the __init__ is the initialization of the student
'''
class Student:
    def __init__(self, name, major, gpa, age, sex, is_on_probation):
        self.name = name
        self.major = major
        self.gpa =gpa
        self.age = age
        self.sex = sex
        self.is_on_probation = is_on_probation